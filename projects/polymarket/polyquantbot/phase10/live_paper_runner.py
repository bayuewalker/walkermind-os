"""Phase 10.8 — LivePaperRunner: Signal Activation & Debug.

Extends Phase 10.4 with:
  - Signal debug logging per tick (SIGNAL_DEBUG_MODE)
  - Signal counter metrics via SignalMetrics
  - Forced test-signal fallback after 30 min silence
  - ActivityMonitor: CRITICAL alerts for signal / trade inactivity
  - Execution-path logging on every simulated order attempt

Original Phase 10.4 description — LivePaperRunner: Real WS + Paper Execution Observation.

Connects to the live Polymarket WebSocket feed and runs the full trading
pipeline in PAPER mode using ExecutionSimulator (no real orders).

Pipeline::

    PolymarketWSClient (real WS)
        │  live orderbook / trade events
        ▼
    LivePaperRunner.event_loop
        │  data_received_ts stamped
        ▼
    OrderBookManager → Phase7MarketCache
        │  microstructure (bid/ask/spread/depth)
        ▼
    decision_callback (optional signal generator)
        │  signal_generated_ts stamped
        ▼
    RiskGuard.check()           ← kill switch / drawdown / daily loss
        │
    GoLiveController            ← forced PAPER mode (allow_execution always False)
        │  (blocks real orders — execution below is simulation only)
        ▼
    ExecutionSimulator.execute()  ← PAPER_LIVE_SIM — orderbook walk fill
        │  fill + slippage recorded in FillTracker
        ▼
    MetricsValidator  (latency, EV, fill_rate, drawdown, slippage)
        │
    TelegramLive  (error / kill / checkpoint alerts)

Background tasks:
    Checkpoint loop — sends Telegram summary at configured intervals (6h/12h/24h)
    Health log      — structured metrics log every health_log_interval_s
    WS reconnect    — handled transparently by PolymarketWSClient + logged

Constraints:
    - ZERO real order execution (ExecutionSimulator.send_real_orders=False enforced)
    - NO bypass risk guard
    - NO crash allowed (all exceptions caught/logged)
    - Deterministic shutdown via stop() or RunController

Usage::

    runner = LivePaperRunner.from_config(config, market_ids=["0xabc..."])
    await runner.start()
    await runner.run()          # blocks until stop() called or 24H elapsed
    report = runner.build_report()

Environment variables:
    CLOB_WS_URL          — WS endpoint (default: wss://clob.polymarket.com)
    TELEGRAM_BOT_TOKEN   — Telegram Bot API token
    TELEGRAM_CHAT_ID     — Telegram target chat ID
    DRY_RUN              — force paper mode ("true")
"""
